     */
    public function update(Request $request, $id)
    {
        $company = ComanyDetails::find($id);
        if ($request->yes_no == "true"){
            $request->yes_no = 1;
        }
        else{
            $request->yes_no = 0;
        }

        $company->update([
            'BKR_registered' => $request->yes_no
        ]);
        return redirect()->route('sales.index');
    }

